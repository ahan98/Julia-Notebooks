md"""
This notebook contains simple examples for the `DataFrames.jl` join operations.

The different join operations can be roughly summarized as follows:

- `outerjoin`: `df1 OR df2`
- `innerjoin`: `df1 AND df2`
- `semijoin`: `df1 AND df2`, with only the columns from `df1`
- `antijoin`: `df1 AND !df2`, with only the columns from `df1`
- `leftjoin`: `df1`
- `rightjoin`: `df2`
- `crossjoin`: Cartesian product of rows from `df1` and `df2`
"""

# ╔═╡ 50b44925-d128-4711-8f8a-26113001e1b3
df1 = DataFrame(A = 1:3, B = ["x", "y", "z"])

# ╔═╡ f5d86f65-18a2-4953-9ff4-56113f5956a9
df2 = DataFrame(A = 2:4, C = ["xx", "yy", "zz"])

# ╔═╡ 3bd1b823-148c-48e3-b600-6c67879474b0
# Include rows with values of A shared by df1 OR df2
outerjoin(df1, df2, on = :A)

# ╔═╡ fcb98045-8d45-4b9c-828c-4b855b5fd315
# Include rows with values of A shared by df1 AND df2
innerjoin(df1, df2, on = :A)

# ╔═╡ ba0e7159-d1e0-468e-8fa9-c4d75c4dcd5a
# Same as innerjoin, except only keep columns from df1
semijoin(df1, df2, on = :A)

# ╔═╡ d99f4d3c-9fb7-428e-8118-71d2fb10df04
# Include rows with values of A shared by df1 AND !df2.
# As in semijoin, only keep columns from df1.
antijoin(df1, df2, on = :A)

# ╔═╡ 3d8518a6-5327-4470-88b9-9262721329c5
# Include rows with values of A shared by df1.
# Rows are ordered as they appear in df1.
leftjoin(df1, df2, on = :A, order = :left)

# ╔═╡ ff67517f-0b9e-4e75-865e-cd28ed663e24
# Include rows with values of A shared by df2.
rightjoin(df1, df2, on = :A)

# ╔═╡ 401c7b60-ea85-433f-aa96-02aaa00ffcd0
# Include cartesian product of rows passed from all data frames.
# That is, if df1 has r1 rows, and df2 has r2 rows, the output
# data frame has r1 x r2 rows.
# 
# Each of the m x n pairs of rows includes all columns from each
# data frame. E.g., if df1 has c1 columns and df2 has c2 columns,
# the output data frame has c1 + c2 columns.
#
# The `makeunique` parameter simply appends a suffix to conflicting
# column names so that column names in the output frame are unique.
crossjoin(df1, df2, makeunique = true)

